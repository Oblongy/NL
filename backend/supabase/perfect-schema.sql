-- Players table - stores all player account data
create table if not exists public.game_players (
  id bigint generated by default as identity primary key,
  username text not null unique,
  password_hash text not null,
  
  -- Currency and progression
  money bigint not null default 50000,
  points bigint not null default 1000000,
  score bigint not null default 0,
  
  -- Profile
  image_id integer not null default 0,
  gender text not null default 'm' check (gender in ('m', 'f')),
  driver_text text not null default '',
  
  -- Status flags
  active integer not null default 1,
  vip integer not null default 0,
  facebook_connected integer not null default 0,
  alert_flag integer not null default 0,
  
  -- Progression
  blackcard_progress integer not null default 0,
  sponsor_rating integer not null default 0,
  respect_level integer not null default 0,
  title_id integer not null default 0,
  track_rank integer not null default 0,
  
  -- Location and UI
  location_id integer not null default 100,
  background_id integer not null default 0,
  
  -- Messaging
  message_badge integer not null default 0,
  
  -- Team (nullable - player may not be in a team)
  team_id bigint,
  team_name text not null default '',
  
  -- Badges (stored as JSON array)
  badges_json jsonb not null default '[]'::jsonb,
  
  -- Default car reference
  default_car_game_id bigint,
  
  -- Role-based access control (5 = normal player, lower = admin)
  client_role integer not null default 5,
  
  -- Timestamps
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

-- Sessions table - tracks active player sessions
create table if not exists public.game_sessions (
  session_key text primary key,
  player_id bigint not null references public.game_players(id) on delete cascade,
  created_at timestamptz not null default timezone('utc', now()),
  last_seen_at timestamptz not null default timezone('utc', now())
);

-- Add expires_at column if it doesn't exist
alter table public.game_sessions 
  add column if not exists expires_at timestamptz not null default timezone('utc', now()) + interval '7 days';

-- Cars table - stores player-owned vehicles
create table if not exists public.game_cars (
  game_car_id bigint generated by default as identity primary key,
  player_id bigint not null references public.game_players(id) on delete cascade,
  
  -- Car identity
  catalog_car_id integer not null,
  selected boolean not null default false,
  
  -- Customization
  paint_index integer not null default 4,
  color_code text not null default 'C0C0C0',
  plate_name text not null default '',
  
  -- Visual state
  locked integer not null default 0,
  aero integer not null default 0,
  image_index integer not null default 0,
  
  -- Parts and wheels (stored as XML)
  wheel_xml text not null default '<ws><w wid=''1'' id=''1001'' ws=''17''/></ws>',
  parts_xml text not null default '',

  -- Test-drive state
  test_drive_invitation_id bigint,
  test_drive_name text,
  test_drive_money_price bigint,
  test_drive_point_price bigint,
  test_drive_expires_at timestamptz,
  
  -- Timestamps
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

alter table public.game_cars add column if not exists test_drive_invitation_id bigint;
alter table public.game_cars add column if not exists test_drive_name text;
alter table public.game_cars add column if not exists test_drive_money_price bigint;
alter table public.game_cars add column if not exists test_drive_point_price bigint;
alter table public.game_cars add column if not exists test_drive_expires_at timestamptz;

-- Add unique partial index for one selected car per player
create unique index if not exists idx_game_cars_one_selected_per_player 
  on public.game_cars(player_id) 
  where selected = true;

-- Teams table - stores team/crew data
create table if not exists public.game_teams (
  id bigint generated by default as identity primary key,
  name text not null unique,
  tag text not null default '',
  description text not null default '',
  
  -- Team owner
  owner_player_id bigint references public.game_players(id) on delete set null,
  
  -- Customization
  avatar_path text not null default '',
  location_id integer not null default 100,
  
  -- Stats
  score bigint not null default 0,
  wins integer not null default 0,
  losses integer not null default 0,
  member_count integer not null default 0,
  
  -- Timestamps
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

-- Team members table - tracks team membership
create table if not exists public.game_team_members (
  id bigint generated by default as identity primary key,
  team_id bigint not null references public.game_teams(id) on delete cascade,
  player_id bigint not null references public.game_players(id) on delete cascade,
  
  -- Member role
  role text not null default 'member' check (role in ('owner', 'admin', 'member')),
  
  -- Contribution tracking
  contribution_score bigint not null default 0,
  
  -- Timestamps
  joined_at timestamptz not null default timezone('utc', now()),
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  
  -- Constraints
  unique (team_id, player_id),
  unique (player_id) -- Player can only be in one team
);

-- Mail table - stores in-game messages
create table if not exists public.game_mail (
  id bigint generated by default as identity primary key,
  recipient_player_id bigint not null references public.game_players(id) on delete cascade,
  sender_player_id bigint references public.game_players(id) on delete set null,
  
  -- Message organization
  folder text not null default 'inbox' check (folder in ('inbox', 'sent', 'trash')),
  message_type text not null default 'player' check (message_type in ('system', 'player', 'team')),
  
  -- Content
  subject text not null default '',
  body text not null default '',
  
  -- Status
  is_read boolean not null default false,
  is_deleted boolean not null default false,
  
  -- Attachments
  attachment_money bigint not null default 0,
  attachment_points bigint not null default 0,
  
  -- Timestamps
  created_at timestamptz not null default timezone('utc', now()),
  read_at timestamptz
);


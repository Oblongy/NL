-- Function to clean up expired sessions
create or replace function public.cleanup_expired_sessions()
returns integer
language plpgsql
as $$
declare
  deleted_count integer;
begin
  delete from public.game_sessions
  where expires_at < timezone('utc', now());
  
  get diagnostics deleted_count = row_count;
  return deleted_count;
end;
$$;

-- Function to update team member count
create or replace function public.update_team_member_count()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    update public.game_teams
    set member_count = member_count + 1
    where id = new.team_id;
  elsif tg_op = 'DELETE' then
    update public.game_teams
    set member_count = member_count - 1
    where id = old.team_id;
  end if;
  return null;
end;
$$;

-- Apply trigger for team member count
drop trigger if exists game_team_members_update_count on public.game_team_members;
create trigger game_team_members_update_count
  after insert or delete on public.game_team_members
  for each row execute function public.update_team_member_count();

